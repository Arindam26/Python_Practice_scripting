"""Sample Input

9 27
Sample Output

------------.|.------------
---------.|..|..|.---------
------.|..|..|..|..|.------
---.|..|..|..|..|..|..|.---
----------WELCOME----------
---.|..|..|..|..|..|..|.---
------.|..|..|..|..|.------
---------.|..|..|.---------
------------.|.------------

"""

n, m = map(int, input().split())
a = [('|||' * (2 * i + 1)).center(m, '-') for i in range(n // 2)]
print('\n'.join(a + ['WELCOME'.center(m, '-')] + a[::-1]))
